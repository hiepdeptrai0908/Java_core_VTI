public class OperatorsDemo {
    public static void main(String[] args) {

        // Toán tử số học
        System.out.println(99 + 2);
        System.out.println(99 - 2);
        System.out.println(99 * 2);
        System.out.println(99 / 2);
        System.out.println(99 % 2);

        // Toán tử quan hệ
        // ==, >, >=, <, <=, !=

        // Toán tử logic
        // AND: &&
        // OR: ||
        // NOT: !
    }
}
